    <div class="charname"><b>§2. Accountsitting</b></div>
     <div class="spacer"></div> 
     Wenn aus bestimmten Gründen ein anderer Account für einen Zeitraum weitergeführt werden soll(sitting), dann ist dies nur mit ausdrücklicher Erlaubnis durch den Support und den Inhaber des Accounts gestattet.
     <div class="spacer"></div>
     Wird eine Erlaubnis ausgesprochen, so darf man den Account nur für allgemeine Zwecke (Training) nutzen. Das gegenseitige Kämpfen oder einen Vorteil aus dieser Sitting-Angelegenheit zu erzielen ist strengstens untersagt.
     <div class="spacer"></div> 
     - Beispiele für verbotene Aktionen: Pms lesen, Pms verfassen, Chat/Kommentar-Beiträge verfassen, Items/Rüstungen löschen. 
     <div class="spacer"></div> 
     Die Gültigkeit von Account-Sitting beruht nur auf das Einstellen von Flügen,Training und das Durchführen von Wertungskämpfen.
     <div class="spacer"></div> 
     Die Kampagne darf ebenfalls weitergeführt werden, wenn der Accountsbesitzer dies erlaubt. 
     <div class="spacer"></div> 
     Der Markthandel ist verboten. (Ausnahme: Senzuhandel). Der Shop darf nur für Heilungszwecke und Trainingszwecke genutzt werden.
     <div class="spacer"></div> 
     - Eine Sitting-Genehmigung gilt nur für eine Spielwelt.
